package com.bary.ui.common.bean;

public class Padding{
        public Padding(int LEFT, int TOP, int RIGHT, int BOTTOM) {
            this.LEFT = LEFT;
            this.TOP = TOP;
            this.RIGHT = RIGHT;
            this.BOTTOM = BOTTOM;
        }

        public int LEFT;
        public int TOP;
        public int RIGHT;
        public int BOTTOM;
    }